import {
  bigint,
  bigserial,
  bit,
  boolean,
  bytea,
  char,
  cidr,
  date,
  decimal,
  doublePrecision,
  geometry,
  halfvec,
  inet,
  integer,
  interval,
  json,
  jsonb,
  line,
  macaddr,
  macaddr8,
  numeric,
  pgEnum,
  pgTable,
  point,
  real,
  serial,
  smallint,
  smallserial,
  sparsevec,
  text,
  time,
  timestamp,
  uuid,
  varchar,
  vector,
} from 'drizzle-orm/pg-core';

export const moodEnum = pgEnum('fixture_pg_data_types_mood', ['sad', 'ok', 'happy']);

export const pgDataTypes = pgTable('pg_data_types', {
  id: serial('id').primaryKey(),
  smallIntCol: smallint('small_int_col'),
  integerCol: integer('integer_col'),
  serialCol: serial('serial_col'),
  smallSerialCol: smallserial('small_serial_col'),
  bigIntNumberCol: bigint('big_int_number_col', { mode: 'number' }),
  bigIntBigIntCol: bigint('big_int_bigint_col', { mode: 'bigint' }),
  bigIntStringCol: bigint('big_int_string_col', { mode: 'string' }),
  bigSerialNumberCol: bigserial('big_serial_number_col', { mode: 'number' }),
  bigSerialBigIntCol: bigserial('big_serial_bigint_col', { mode: 'bigint' }),
  realCol: real('real_col'),
  doublePrecisionCol: doublePrecision('double_precision_col'),
  numericStringCol: numeric('numeric_string_col'),
  numericNumberCol: numeric('numeric_number_col', { mode: 'number' }),
  numericBigIntCol: numeric('numeric_bigint_col', { mode: 'bigint' }),
  decimalStringCol: decimal('decimal_string_col'),
  decimalNumberCol: decimal('decimal_number_col', { mode: 'number' }),
  decimalBigIntCol: decimal('decimal_bigint_col', { mode: 'bigint' }),
  booleanCol: boolean('boolean_col'),
  textCol: text('text_col'),
  textEnumCol: text('text_enum_col', { enum: ['alpha', 'beta', 'gamma'] }),
  varcharCol: varchar('varchar_col', { length: 64 }),
  varcharEnumCol: varchar('varchar_enum_col', { enum: ['red', 'green', 'blue'], length: 16 }),
  charCol: char('char_col', { length: 3 }),
  charEnumCol: char('char_enum_col', { enum: ['AA', 'AB', 'AC'], length: 2 }),
  uuidCol: uuid('uuid_col'),
  byteaCol: bytea('bytea_col'),
  jsonCol: json('json_col'),
  jsonbCol: jsonb('jsonb_col'),
  dateCol: date('date_col', { mode: 'date' }),
  dateStringCol: date('date_string_col', { mode: 'string' }),
  timeCol: time('time_col'),
  timeTzCol: time('time_tz_col', { withTimezone: true }),
  timestampCol: timestamp('timestamp_col'),
  timestampStringCol: timestamp('timestamp_string_col', { mode: 'string' }),
  timestampTzCol: timestamp('timestamp_tz_col', { withTimezone: true }),
  timestampTzStringCol: timestamp('timestamp_tz_string_col', {
    withTimezone: true,
    mode: 'string',
  }),
  intervalCol: interval('interval_col'),
  cidrCol: cidr('cidr_col'),
  inetCol: inet('inet_col'),
  macaddrCol: macaddr('macaddr_col'),
  macaddr8Col: macaddr8('macaddr8_col'),
  pointTupleCol: point('point_tuple_col'),
  pointObjectCol: point('point_object_col', { mode: 'xy' }),
  lineTupleCol: line('line_tuple_col'),
  lineAbcCol: line('line_abc_col', { mode: 'abc' }),
  geometryTupleCol: geometry('geometry_tuple_col', { type: 'point' }),
  geometryObjectCol: geometry('geometry_object_col', { type: 'point', mode: 'xy' }),
  vectorCol: vector('vector_col', { dimensions: 3 }),
  halfvecCol: halfvec('halfvec_col', { dimensions: 3 }),
  sparsevecCol: sparsevec('sparsevec_col', { dimensions: 3 }),
  bitCol: bit('bit_col', { dimensions: 8 }),
  moodCol: moodEnum('mood_col'),
});
